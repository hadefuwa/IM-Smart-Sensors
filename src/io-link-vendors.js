export function resolveVendorName(vendorId) {
  return VENDOR_NAMES[vendorId] || (vendorId ? `Vendor ${vendorId}` : '—');
}

export function resolveDeviceName(vendorId, deviceId, rawName) {
  const key = `${vendorId}/${deviceId}`;
  return DEVICE_NAMES[key] || rawName || (deviceId ? `Device ${deviceId}` : '—');
}
